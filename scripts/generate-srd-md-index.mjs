#!/usr/bin/env node
import { readdirSync, readFileSync, statSync, writeFileSync, existsSync } from "node:fs";
import { join, resolve } from "node:path";

const ROOT = resolve(process.cwd(), "src", "data", "srd");
const TYPES = ["classes", "races", "subclasses", "backgrounds", "feats"];
const OUT_FILE = resolve(ROOT, "index.generated.ts");

function extractField(raw, key) {
  const m = raw.match(new RegExp(`^${key}:\\s*(.+)$`, "m"));
  if (!m) return undefined;
  return m[1].trim().replace(/^["']|["']$/g, "");
}

const entries = [];
for (const type of TYPES) {
  const dir = join(ROOT, type);
  if (!existsSync(dir)) continue;
  const files = readdirSync(dir).filter((f) => f.endsWith(".md")).sort();
  for (const f of files) {
    if (!statSync(join(dir, f)).isFile()) continue;
    const raw = readFileSync(join(dir, f), "utf8");
    const slug = extractField(raw, "slug");
    const name = extractField(raw, "name");
    if (!slug || !name) {
      throw new Error(`${join(dir, f)} is missing slug or name in frontmatter`);
    }
    entries.push({ type, slug, name, path: `./${type}/${f}` });
  }
}

const importLines = entries.map((e, i) => `import md${i} from ${JSON.stringify(e.path)};`).join("\n");
const entryLines = entries.map((e, i) => `  { type: "${e.type}", slug: ${JSON.stringify(e.slug)}, name: ${JSON.stringify(e.name)}, content: md${i} },`).join("\n");

const content = `/* AUTO-GENERATED by scripts/generate-srd-md-index.mjs — DO NOT EDIT BY HAND. */
${importLines}

export interface SrdMdEntry {
  type: string;
  slug: string;
  name: string;
  content: string;
}

export const SRD_MD_ENTRIES: SrdMdEntry[] = [
${entryLines}
];
`;

writeFileSync(OUT_FILE, content, "utf8");
console.log(`Wrote ${OUT_FILE} with ${entries.length} entries`);
