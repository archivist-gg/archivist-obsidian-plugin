#!/usr/bin/env node
import { readdirSync, statSync, writeFileSync, existsSync } from "node:fs";
import { join, resolve } from "node:path";

const ROOT = resolve(process.cwd(), "src", "data", "srd");
const TYPES = ["classes", "races", "subclasses", "backgrounds", "feats"];
const OUT_FILE = resolve(ROOT, "index.generated.ts");

const entries = [];
for (const type of TYPES) {
  const dir = join(ROOT, type);
  if (!existsSync(dir)) continue;
  const files = readdirSync(dir).filter((f) => f.endsWith(".md"));
  for (const f of files) {
    if (!statSync(join(dir, f)).isFile()) continue;
    const slug = f.replace(/\.md$/, "");
    entries.push({ type, slug, path: `./${type}/${f}` });
  }
}

const importLines = entries.map((e, i) => `import md${i} from "${e.path}";`).join("\n");
const entryLines = entries.map((e, i) => `  { type: "${e.type}", slug: ${JSON.stringify(e.slug)}, content: md${i} },`).join("\n");

const content = `/* AUTO-GENERATED by scripts/generate-srd-md-index.mjs — DO NOT EDIT BY HAND. */
${importLines}

export interface SrdMdEntry {
  type: string;
  slug: string;
  content: string;
}

export const SRD_MD_ENTRIES: SrdMdEntry[] = [
${entryLines}
];
`;

writeFileSync(OUT_FILE, content, "utf8");
console.log(`Wrote ${OUT_FILE} with ${entries.length} entries`);
